    public void createTable() {
        int nRows = model.getRowCount();
        for (int i = 0; i < nRows; i++) {
            model.removeRow(0);
        }
        for (Map.Entry<String, Card> entry : otherCards.entrySet()) {
            model.addRow(new Object[]{entry.getKey()});
        }
    }
    
